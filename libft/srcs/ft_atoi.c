int	ft_atoi(const char *nptr)
{
	unsigned long int	r;
	int					s;

	r = 0;
	s = 1;
	while (*nptr == '\t' || *nptr == '\n' || *nptr == '\v'
		|| *nptr == '\f' || *nptr == '\r' || *nptr == ' ')
		nptr++;
	if (*nptr == '+' || *nptr == '-')
		if (*(nptr++) == '-')
			s = -s;
	while (*nptr <= '9' && *nptr >= '0')
	{
		r = r * 10 + *(nptr++) - '0';
		if (r > 2147483647 && s == 1)
			return (-1);
		if (r > 2147483648 && s == -1)
			return (0);
	}
	return (r * s);
}
