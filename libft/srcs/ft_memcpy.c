#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	char	*d;
	char	*s;

	d = (char *)dest;
	s = (char *)src;
	if (!d && !s)
		return (NULL);
	while (n-- > 0)
		*(d++) = *(s++);
	return (dest);
}
